#include <stdio.h>

int main(void)
{
	printf("\'"); /* Single Quote, Apostrophe */
	printf("\""); /* " */
	printf("\?"); /* Question Mark */
	printf("\\"); /* Backslash */
	printf("\a"); /* Alert or Bell */
	printf("\b"); /* Backspace */
	printf("\f"); /* Form Feed */
	printf("\n"); /* Line Feed */
	printf("\r"); /* Carriage Return */
	printf("\t"); /* Horizontal Tab */
	printf("\v"); /* Vertical Tab */

	return 0;
}

